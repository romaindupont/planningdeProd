
export function dayName(numerojour) {
  if (numerojour==0) {
    return "Dimanche";
  }
  if (numerojour==1) {
    return "Lundi";
  }
  if (numerojour==2) {
    return "Mardi";
  }
  if (numerojour==3) {
    return "Mercredi";
  }
  if (numerojour==4) {
    return "Jeudi";
  }
  if (numerojour==5) {
    return "Vendredi";
  }
  if (numerojour==6) {
    return "Samedi";
  }
};
